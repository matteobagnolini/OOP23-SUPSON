package supson.model.block;

/**
 * Enum representing different types of blocks in the game.
 */
public enum BlockType {
    /**
     * Represents terrain blocks.
     */
    TERRAIN,

    /**
     * Represents power-up blocks.
     */
    POWER_UP,

    /**
     * Represents ring blocks.
     */
    RING,

    /**
     * Represents trap blocks.
     */
    TRAP

}
